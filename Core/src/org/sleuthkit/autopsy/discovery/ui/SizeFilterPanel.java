    @ThreadConfined(type = ThreadConfined.ThreadType.AWT)
    @Override
    void configurePanel(boolean selected, List<?> selectedItems) {
        sizeCheckbox.setSelected(selected);
        if (sizeCheckbox.isEnabled() && sizeCheckbox.isSelected()) {
            sizeList.setEnabled(true);
            if (selectedItems != null) {
                List<SearchData.FileSize> szList = new ArrayList<>();
                for (Object item : selectedItems) {
                    if (item instanceof SearchData.FileSize) {
                        szList.add((SearchData.FileSize) item);
                    }
                }
                if (!szList.isEmpty()) {
                    sizeList.setSelectedElements(szList);
                }
            }
        } else {
            sizeList.setEnabled(false);
        }
    }

